#include "Switch.h"
#include <Arduino.h>

/****************************************************
 *  Constructor / destructor                        *
 ****************************************************/

Switch::Switch(int idValue, int pinValue) : id(idValue), pin(pinValue) {}

Switch::~Switch() {}

/****************************************************
 *  Member functions                                *
 ****************************************************/

bool Switch::isPressed() {
    return (digitalRead(pin) == LOW) ? true : false;
}